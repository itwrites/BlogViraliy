export function getTranslation(language: ContentLanguage | string, key: TranslationKey): string {
  const lang = (language || "en") as ContentLanguage;
  const langTranslations = translations[lang] || translations.en;
  return langTranslations[key] || translations.en[key] || key;
}

export function getTranslations(language: ContentLanguage | string): Translations {
  const lang = (language || "en") as ContentLanguage;
  return translations[lang] || translations.en;
}

export function formatDateLocalized(date: Date | string, language: ContentLanguage | string): string {
  const lang = (language || "en") as string;
  const localeMap: Record<string, string> = {
    en: "en-US",
    es: "es-ES",
    fr: "fr-FR",
    de: "de-DE",
    it: "it-IT",
    pt: "pt-PT",
    "pt-br": "pt-BR",
    nl: "nl-NL",
    sv: "sv-SE",
    no: "nb-NO",
    da: "da-DK",
    fi: "fi-FI",
    pl: "pl-PL",
    sk: "sk-SK",
    tr: "tr-TR",
    ru: "ru-RU",
    ar: "ar-SA",
    zh: "zh-CN",
    ja: "ja-JP",
    ko: "ko-KR",
    hi: "hi-IN",
  };
  
  const locale = localeMap[lang] || "en-US";
  
  return new Date(date).toLocaleDateString(locale, {
    month: "short",
    day: "numeric",
    year: "numeric",
  });
}
